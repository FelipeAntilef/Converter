package javaapplication3;

public class Convercionesmonedas {
    public static double pesoDolar(double valor) {
        return valor * 0.0012;
    }

    public static double pesoEuro(double valor) {
        return valor * 0.0011;
    }

    public static double pesoLibra(double valor) {
        return valor * 0.00093;
    }

    public static double pesoYen(double valor) {
        return valor * 0.17;
    }

    public static double pesoWon(double valor) {
        return valor * 1.54;
    }

    public static double DolarPeso(double valor) {
        return valor * 828.16;
    }

    public static double EuroPeso(double valor) {
        return valor * 919.09;
    }

    public static double libraPes(double valor) {
        return valor * 1069.88;
    }

    public static double yenPesp(double valor) {
        return valor * 5.9;
    }

    public static double WonPeso(double valor) {
        return valor * 0.65;
    }
}